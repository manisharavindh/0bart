size = 7

while True:
    user_input = input().upper()
    print()
    if user_input == 'E':
        break
    else:
        output = ['' for _ in range(size)]

        for chr in user_input:
            if chr in ins:
                index = ins.index(chr)
                for i in range(size):
                    output[i] += outs[index][i]
            else:
                for i in range(size):
                    output[i] += ' ' * 5

        for line in output:
            for ch in line:
                if ch == '0':
                    print(f"{SEC}{ch}{RESET}", end='')
                else:
                    print(f"{PRI}{ch}{RESET}", end='')
            print(end='\n')
        print()